package back;

public class Empresa {
	
	private String nome;
	private String cnpj;
	private String cargos;
	private String competenciasExigidas;
	
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getCnpj() {
		return cnpj;
	}
	public void setCnpj(String cnpj) {
		this.cnpj = cnpj;
	}
	public String getCargos() {
		return cargos;
	}
	public void setCargos(String cargos) {
		this.cargos = cargos;
	}
	public String getCompetenciasExigidas() {
		return competenciasExigidas;
	}
	public void setCompetenciasExigidas(String competenciasExigidas) {
		this.competenciasExigidas = competenciasExigidas;
	}
	
	
}
